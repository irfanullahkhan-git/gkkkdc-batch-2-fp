#include<iostream>
using namespace std;

int main(){
	// operators: +, -, *, /, %
	int n1 = 15, n2 = 15;
	int result = 12;
	
	result = n1 + n2; 
	cout<< result<<endl; 
	result = n1 - n2;
	cout<< result<<endl;
	result = n1 * n2;
	cout<< result<<endl;
	result = n1 / n2;
	cout<<result<<endl;
	result = n1 % n2;
	cout<<result<<endl;
}
